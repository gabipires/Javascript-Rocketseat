// Condição ternária, somente um IF e um ELSE

var sexo = 'M';

if(sexo === 'M'){
    return 'Masculino'

}else{
    return 'Feminino'
}


// Podemos simplificar uma condição ternária da seguinte forma:

var retorno =  (sexo === 'M') ? 'Masculino' : 'Feminino'
console.log(retorno)

// condição ? condição se verdadeiro : condição se falso
