// Intervalo - uma função que executa várias vezes dentro de um intervalo

function exibeAlgo(){
    console.log("Hello WOrd")
    
}

setInterval(exibeAlgo, 1000)
// a cada um segundo a função vai executar e imprimir "Hello Word!"


// Time Out - executa apenas uma vez, com um delay de X segundos antes de executar a função
function exibeAlgo(){
    console.log("Hello WOrd")
    
}

setTimeout(exibeAlgo, 5000)
// Depois de 5 segundos ele vai executar a função