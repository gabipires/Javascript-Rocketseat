//FOR --> sempre que a gente souber o tamanho do intervalo, a condição final.

// valor inicial -- condição -- execução

for (var i = 0; i <= 100; i++){
    console.log(i);
}



// WHILE --> usamos quando não sabemos quantas vezes o fluxo vai ser executado.

j = 0;
while (j <= 100){
    console.log(j);
    j++;

}